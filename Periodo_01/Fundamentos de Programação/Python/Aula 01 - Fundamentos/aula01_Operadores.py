numero1 = 6
numero2 = 2
numero3 = 10

# Operadores

print((numero1 > numero2) & (numero3 != numero2)) # E (and &)
print((numero1 == 5) | (numero3 >= numero2)) # Ou (or |)
print((numero1 > numero2) ^ (numero3 != numero2)) # XOU (^) (Dois verdadeiros ou falsos = false)
print(not(numero1 > numero2) and (numero3 != numero2)) # Negação (not)

print(numero1 + numero2) # Soma
print(numero1 - numero2) # Subtração 
print(numero1 * numero2) # Multiplicação
print(numero1 / numero2) # Divisão
print(numero1 // numero2) #  Divisão inteira
print(numero1 % numero2) # Resto da divisão
print(numero1 ** numero2) # Exponencial

print(numero1 > numero2) # Maior que
print(numero1 < numero2) # Menor que
print(numero1 == numero2) # Igual
print(numero1 != numero2) # Diferente
print(numero1 <= numero2) # Menor ou igual que
print(numero1 >= numero2) # Maior ou igual que